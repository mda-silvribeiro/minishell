#include "libft.h"

void	ft_sort_int(int *res, int size)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (i < size)
	{
		while (j < size)
		{
			if (res[j] < res[i])
				ft_swap(&res[j], &res[i]);
			j++;
		}
		j = 0;
		i++;
	}
}
